# Force literal block scalars for any string with newlines, folded for very long strings
class LiteralStr(str):
    pass


class FoldedStr(str):
    pass


def _literal_representer(dumper, data):
    return dumper.represent_scalar("tag:yaml.org,2002:str", data, style="|")


def _folded_representer(dumper, data):
    return dumper.represent_scalar("tag:yaml.org,2002:str", data, style=">")


yaml.add_representer(LiteralStr, _literal_representer)
yaml.add_representer(FoldedStr, _folded_representer)


def L(s):
    """Literal block scalar — preserves line breaks, used for multi-paragraph text."""
    return LiteralStr(s.strip())


def F(s):
    """Folded scalar — wraps long lines, used for long single-paragraph text."""
    return FoldedStr(" ".join(s.split()))


